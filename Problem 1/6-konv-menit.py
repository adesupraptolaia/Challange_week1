n = 63

if n % 60 == 0:
    k = int(n/60)
    print(str(k)+":00")
elif n < 60:
    print("0:"+str(n))
else:
    m = n % 60
    k = n - m
    j = int(k/60)
    if m <= 10:
        print(str(j)+":0"+str(m))
    else:
        print(str(j)+":"+str(m))


