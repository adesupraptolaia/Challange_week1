n = 3 
count=0

if n > 2:
    for i in range(1, n+1):
        if n%i ==0:
            count += 1

    if count == 2:
        print("Bilangan Prima")
    else:
        print("Bukan Bilangan Prima")
elif n ==2:
    print("Bilangan Prima")
else:
    print("Bukan bilangan prima")

